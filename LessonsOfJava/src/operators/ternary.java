package operators;

public class ternary {
	public static void main(String[] args) {
	    int a=5;
		int b=3;
		System.out.println(a<b ? 5:6);
		System.out.println(a>b ? 8:6);
		System.out.println(a<=b ? 5:6);
		System.out.println(a>=b ? 9:6);
		System.out.println(a!=b ? 5:6);
		System.out.println(a==b ? 8:6);
		
	}

}
